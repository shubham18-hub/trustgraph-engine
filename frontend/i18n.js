// Current language
let currentLang = localStorage.getItem('preferredLanguage') || 'hi';

// Get translation
function t(key) {
    return translations[currentLang][key] || translations['hi'][key] || key;
}

// Set language
function setLanguage(lang) {
    if (translations[lang]) {
        currentLang = lang;
        localStorage.setItem('preferredLanguage', lang);
        updatePageText();
        
        // Update HTML lang attribute
        document.documentElement.lang = lang;
        
        console.log('Language changed to:', lang);
    }
}

// Get current language
function getCurrentLanguage() {
    return currentLang;
}

// Get all available languages
function getAvailableLanguages() {
    return Object.keys(translations).map(code => ({
        code: code,
        name: translations[code].name,
        flag: translations[code].flag
    }));
}

// Update all text on page
function updatePageText() {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = t(key);
        
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = translation;
        } else {
            el.textContent = translation;
        }
    });
    
    // Update all elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = t(key);
    });
    
    // Update all elements with data-i18n-title attribute
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        el.title = t(key);
    });
}

// Initialize i18n on page load
window.addEventListener('load', () => {
    updatePageText();
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { t, setLanguage, getCurrentLanguage, getAvailableLanguages, translations };
}
